# O "int()" determina que o input recebido é um número
numero = int(input("Digite um número: "))
numero2 = int(input("Digite um segundo número: "))

# A logica para somar os número
total = numero + numero2

print(f"A soma dos número acima é {total}")
# numero: 10
# numero2: 5
